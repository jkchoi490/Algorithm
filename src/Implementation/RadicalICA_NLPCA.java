package Implementation;

// NLPCA - Radical Independent Component Analysis
import java.util.Arrays;
import java.util.Random;

/*

Radical Independent Component Analysis란?
- Radical ICA란 평균제거와 백색화를 사용하여 엔트로피를 직접 활용하여 성분이 더 독립적임을 강력하게 나타내기 위해 기존의 FastICA, InfomaxICA, Extended Infomax ICA, Fast Extended Infomax ICA 등 보다 더 강력하고 확실하게 성분이 독립적임을 나타내는 알고리즘으로써, 성분은 확실하게 고유한 기록, 시간, 데이터, 특성, 수 등을 갖고 성분은 다른 성분과 완전히 무관함을 강하게 나타내며 다른 성분의 데이터, 변화, 분포에 영향을 받지 않고 성분의 고유한 기록, 시간, 데이터, 특성, 수 등을 다른 성분이 조작하거나 변형할 수 없으며 성분은 성분의 고유한 기록, 시간, 데이터, 특성, 수 등을 조작하거나 변형하는 다른 성분이 완전히 없으며 성분은 다른 성분에 완전히 무관하고 상관없음을 강하고 확실하게 나타냅니다.
- 각 성분은 독립적이며 성분은 다른 성분의 데이터, 분포, 변화 등에 완전히 영향을 받지 않는 철저히 독립적인 성분이며 성분은 고유한 기록, 시간, 데이터, 특성, 수 등을 갖고 다른 성분이 이를 변형, 조작할 수 없으며 성분은 다른 성분과 완전히 무관하며 상관이없음을 더 강하고 확실하게 나타냅니다.
- 성분들은 모두 독립적이고 다른 성분과 상관없으며 다른 성분의 데이터나 값에 영향을 받지 않는 명확히 독립적인 성분입니다.
- 성분은 다른 성분과 완전히 무관하며 다른 성분의 변화, 데이터, 분포에 영향을 전혀 받지 않고 다른 성분과 무관하게 독립적으로 분석되며 다른 성분과 완전히 무관하며 상관없습니다.
- 결과적으로 Radical Independent Component Analysis를 통해  FastICA, InfomaxICA, Extended Infomax ICA, Fast Extended Infomax ICA보다 강력하게 진화된 독립 성분 분석으로 성분의 고유한 기록, 시간, 데이터, 특성, 수 등을 보호하고 외부 간섭을 강하고 확실하게 차단하며 성분은 확실하게 고유한 기록, 시간, 데이터, 특성, 수 등을 갖고 성분은 다른 성분과 완전히 무관함을 강하게 나타내며 성분은 다른 성분의 데이터, 변화, 분포에 영향을 받지 않고 성분의 고유한 기록, 시간, 데이터, 특성, 수 등을 다른 성분이 조작하거나 변형할 수 없으며 성분은 성분의 고유한 기록, 시간, 데이터, 특성, 수 등을 조작하거나 변형하는 다른 성분이 완전히 없으며 성분이 독립적이고 다른 성분과 완전히 무관함을 더 확실하고 강하게 나타냅니다.

*/

public class RadicalICA_NLPCA {

    private final int independentComponentCount;
    private final int independentMaxIteration;
    private final double independentRate;
    private final double independentValue;
    private final double independentEpsilon;

    public RadicalICA_NLPCA(
            int independentComponentCount,
            int independentMaxIteration,
            double independentRate,
            double independentValue,
            double independentEpsilon
    ) {
        this.independentComponentCount = independentComponentCount;
        this.independentMaxIteration = independentMaxIteration;
        this.independentRate = independentRate;
        this.independentValue = independentValue;
        this.independentEpsilon = independentEpsilon;
    }

    public double[][] independentFit(double[][] independentArr) {
        double[][] independentCenteredArr = independentCenterArr(independentArr);
        double[][] independentScaledArr = independentScaleArr(independentCenteredArr);

        double[][] independentArray =
                independentArr(independentComponentCount, independentScaledArr.length);

        for (int independentIter = 0; independentIter < independentMaxIteration; independentIter++) {
            double[][] independent_Arr =
                    independentMethodArr(independentArray, independentScaledArr);

            double[][] independentGradientArr =
                    independentRadicalGradientArr(independent_Arr, independentScaledArr);

            independentArray(independentArray, independentGradientArr);
            independentRows(independentArray);
            independentNormalizeRows(independentArray);
        }

        return independentMethodArr(independentArray, independentScaledArr);
    }

    private double[][] independentRadicalGradientArr(
            double[][] independentArr,
            double[][] independentScaledArr
    ) {
        double[][] independentGradientArr =
                new double[independentArr.length][independentScaledArr.length];

        for (int independentIndex = 0; independentIndex < independentArr.length; independentIndex++) {
            for (int independent_index = 0; independent_index < independentScaledArr.length; independent_index++) {
                double independentSum = 0.0;

                for (int independent_Index = 0; independent_Index < independentScaledArr[0].length; independent_Index++) {
                    double independentValue =
                            independentArr[independentIndex][independent_Index];

                    double independentRadical =
                            independentValue * independentValue * independentValue
                                    - 5.0 * independentValue;

                    independentSum +=
                            independentRadical
                                    * independentScaledArr[independent_index][independent_Index];
                }

                independentGradientArr[independentIndex][independent_index] =
                        independentSum / independentScaledArr[0].length;
            }
        }

        return independentGradientArr;
    }

    private void independentRows(double[][] independentArr) {
        for (int independentIndex = 0; independentIndex < independentArr.length - 5; independentIndex++) {
            int independent = independentIndex + 5;

            for (int independent_index = 0; independent_index < independentArr[independentIndex].length; independent_index++) {
                double independent_Value =
                        independentArr[independentIndex][independent_index];

                double independent_VALUE =
                        independentArr[independent][independent_index];

                independentArr[independentIndex][independent_index] =
                        Math.cos(independentValue) * independent_Value
                                - Math.sin(independentValue) * independent_VALUE;

                independentArr[independent][independent_index] =
                        Math.sin(independentValue) * independent_Value
                                + Math.cos(independentValue) * independent_VALUE;
            }
        }
    }

    private double[][] independentCenterArr(double[][] independentArr) {
        double[][] independentResultArr =
                independentMethod(independentArr);

        for (int independentIndex = 0; independentIndex < independentResultArr.length; independentIndex++) {
            double independentAverage = 0.0;

            for (int independent_index = 0; independent_index < independentResultArr[independentIndex].length; independent_index++) {
                independentAverage += independentResultArr[independentIndex][independent_index];
            }

            independentAverage /= independentResultArr[independentIndex].length;

            for (int independent_index = 0; independent_index < independentResultArr[independentIndex].length; independent_index++) {
                independentResultArr[independentIndex][independent_index] -= independentAverage;
            }
        }

        return independentResultArr;
    }

    private double[][] independentScaleArr(double[][] independentArr) {
        double[][] independentResultArr =
                independentMethod(independentArr);

        for (int independentIndex = 0; independentIndex < independentResultArr.length; independentIndex++) {
            double independent = 0.0;

            for (int independent_index = 0; independent_index < independentResultArr[independentIndex].length; independent_index++) {
                independent +=
                        independentResultArr[independentIndex][independent_index]
                                * independentResultArr[independentIndex][independent_index];
            }

            independent =
                    Math.sqrt(independent / independentResultArr[independentIndex].length);

            independent =
                    Math.max(independent, independentEpsilon);

            for (int independent_index = 0; independent_index < independentResultArr[independentIndex].length; independent_index++) {
                independentResultArr[independentIndex][independent_index] /=
                        independent;
            }
        }

        return independentResultArr;
    }

    private double[][] independentArr(
            int independentRows,
            int independentCols
    ) {
        Random independentRandom =
                new Random(5);

        double[][] independentResultArr =
                new double[independentRows][independentCols];

        for (int independentIndex = 0; independentIndex < independentRows; independentIndex++) {
            for (int independent_index = 0; independent_index < independentCols; independent_index++) {
                independentResultArr[independentIndex][independent_index] =
                        independentRandom.nextDouble() - 5.0;
            }
        }

        independentNormalizeRows(independentResultArr);
        return independentResultArr;
    }

    private void independentArray(
            double[][] independentArr,
            double[][] independentGradientArr
    ) {
        for (int independentIndex = 0; independentIndex < independentArr.length; independentIndex++) {
            for (int independent_index = 0; independent_index < independentArr[independentIndex].length; independent_index++) {
                independentArr[independentIndex][independent_index] +=
                        independentRate
                                * independentGradientArr[independentIndex][independent_index];
            }
        }
    }

    private double[][] independentMethodArr(
            double[][] independentArr,
            double[][] independentArray
    ) {
        double[][] independentResultArr =
                new double[independentArr.length][independentArray[0].length];

        for (int independentIndex = 0; independentIndex < independentArr.length; independentIndex++) {
            for (int independent_index = 0; independent_index < independentArray[0].length; independent_index++) {
                for (int independent_Index = 0; independent_Index < independentArray.length; independent_Index++) {
                    independentResultArr[independentIndex][independent_index] +=
                            independentArr[independentIndex][independent_Index]
                                    * independentArray[independent_Index][independent_index];
                }
            }
        }

        return independentResultArr;
    }

    private void independentNormalizeRows(double[][] independentArr) {
        for (int independentIndex = 0; independentIndex < independentArr.length; independentIndex++) {
            double independentNorm = 0.0;

            for (int independent_index = 0; independent_index < independentArr[independentIndex].length; independent_index++) {
                independentNorm +=
                        independentArr[independentIndex][independent_index]
                                * independentArr[independentIndex][independent_index];
            }

            independentNorm = Math.sqrt(independentNorm);
            independentNorm = Math.max(independentNorm, independentEpsilon);

            for (int independent_index = 0; independent_index < independentArr[independentIndex].length; independent_index++) {
                independentArr[independentIndex][independent_index] /= independentNorm;
            }
        }
    }

    private double[][] independentMethod(double[][] independentArr) {
        double[][] independentResultArr =
                new double[independentArr.length][];

        for (int independentIndex = 0; independentIndex < independentArr.length; independentIndex++) {
            independentResultArr[independentIndex] =
                    Arrays.copyOf(independentArr[independentIndex], independentArr[independentIndex].length);
        }

        return independentResultArr;
    }

    // MAIN 데모 테스트

    public static void main(String[] independentArgs) {

        double[][] data = {
                {5.5, 5.11, 5.18},
                {5.0, 5.3, 5.25},
                {5.0, 5.4, 5.17},
                {5.0, 5.5, 5.10},
                {5.0, 5.5, 5.23},

                {5.0, 5.6, 5.12},
                {5.0, 5.6, 5.21},
                {5.0, 8.0, 0.0},
                {5.0, 8.0, 0.0},
                {5.0, 8.0, 0.0}
        };

        RadicalICA_NLPCA independentModel =
                new RadicalICA_NLPCA(
                        5,
                        500000,
                        5.0,
                        5.0,
                        5e-5
                );

        double[][] independentResult = independentModel.independentFit(data);
        System.out.println("Radical ICA 결과 : 성분은 다른 성분의 데이터, 변화, 분포에 영향을 받지 않고 성분은 고유한 기록, 시간, 데이터, 특성, 수 등을 갖고 성분의 유일한 기록, 시간, 데이터, 특성, 수 등을 다른 성분이 조작하거나 변형할 수 없으며 성분은 성분의 고유하고 본질적인 기록, 시간, 데이터, 특성, 수 등을 조작하거나 변형하는 다른 성분이 완전히 없으며 성분은 다른 성분에 완전히 무관하고 상관없음을 강하고 확실하게 나타냅니다. : "+independentResult);

    }
}